package bean;

/**
 * @author Snowson Zhang
 */
public class LinkNode {
    private String data;
    private LinkNode next;

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public LinkNode getNext() {
        return next;
    }

    public void setNext(LinkNode next) {
        this.next = next;
    }
}
